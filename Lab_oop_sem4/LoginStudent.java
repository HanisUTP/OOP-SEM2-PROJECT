public class LoginStudent{
    // variable
    String name;
    int id;
    String email;
    String pwd;
    void setName(String name){
        this.name = name;
    }
    void setId(int id){
        this.id = id;
    }
    void setEmail(String email){
        this.email = email;
    }
    void setPwd(String pwd){
        this.pwd = pwd;
    }
    public String getName(){
        return name;
    }
    public int getId(){
        return id;
    }
    public String getEmail(){
        return email;
    }
    public String getPwd(){
        return pwd;
    }




}